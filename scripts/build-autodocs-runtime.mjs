#!/usr/bin/env node
// Build the Obra Autodocs "runtime body" from the vendored plugin code.js.
//
// The vendored code.js is the full Figma plugin: hoisted function/const
// declarations PLUS three top-level side-effecting statements that drive the
// plugin UI. Those three MUST be removed before we can run generate() inside
// the Desktop Bridge sandbox, because two of them (figma.showUI and
// figma.ui.onmessage = ...) would hijack the Bridge's own UI/message channel
// and break the live connection.
//
// This script removes exactly those three statements (located by anchor, then
// brace/paren matched) and writes the remaining declarations to
// autodocs-body.js. The MCP tool appends its own entry shim (font load +
// getComponentSet + generate/remove) at call time.
//
// If code.js is re-vendored and an anchor no longer matches, this script
// throws — surfacing the drift loudly instead of producing a corrupt body.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { execSync } from "node:child_process";

const __dirname = dirname(fileURLToPath(import.meta.url));
const VENDOR = join(__dirname, "..", "src", "vendor", "obra-autodocs");
const SRC = join(VENDOR, "code.js");
const OUT = join(VENDOR, "autodocs-body.js");

const src = readFileSync(SRC, "utf8");

// The three top-level statements to remove, identified by an exact start anchor.
const ANCHORS = [
	"_fontLoadPromise = Promise.all([",
	"if (_command === 'open') {",
	"figma.ui.onmessage = async (msg) => {",
];

// Walk forward from `start` consuming a single complete top-level statement,
// honoring strings and comments. Returns the index just past the statement.
// - statements terminated by `;` at depth 0 end at that `;`
// - block statements end at the `}` that returns depth to 0, unless the next
//   meaningful token is else/catch/finally (then they continue)
function endOfStatement(text, start) {
	let i = start;
	let depth = 0;
	let opened = false;
	const n = text.length;

	function skipTrivia(j) {
		// skip whitespace and comments, return index of next meaningful char
		while (j < n) {
			const c = text[j];
			if (c === " " || c === "\t" || c === "\n" || c === "\r") { j++; continue; }
			if (c === "/" && text[j + 1] === "/") { while (j < n && text[j] !== "\n") j++; continue; }
			if (c === "/" && text[j + 1] === "*") { j += 2; while (j < n && !(text[j] === "*" && text[j + 1] === "/")) j++; j += 2; continue; }
			break;
		}
		return j;
	}

	while (i < n) {
		const c = text[i];
		// line comment
		if (c === "/" && text[i + 1] === "/") { while (i < n && text[i] !== "\n") i++; continue; }
		// block comment
		if (c === "/" && text[i + 1] === "*") { i += 2; while (i < n && !(text[i] === "*" && text[i + 1] === "/")) i++; i += 2; continue; }
		// strings (single, double, template) — skip with escape handling
		if (c === "'" || c === '"' || c === "`") {
			const q = c; i++;
			while (i < n) {
				if (text[i] === "\\") { i += 2; continue; }
				if (text[i] === q) { i++; break; }
				i++;
			}
			continue;
		}
		if (c === "(" || c === "[" || c === "{") { depth++; opened = true; i++; continue; }
		if (c === ")" || c === "]") { depth--; i++; continue; }
		if (c === "}") {
			depth--;
			i++;
			// Only a closing brace at depth 0 can terminate a block statement.
			// (A closing ) or ] at depth 0 is just the end of a condition or a
			// chained call argument list — the statement continues.)
			if (depth === 0 && opened) {
				const j = skipTrivia(i);
				const rest = text.slice(j, j + 8);
				if (/^else\b/.test(rest) || /^catch\b/.test(rest) || /^finally\b/.test(rest)) {
					continue;
				}
				// consume a trailing semicolon if present (e.g. `};`)
				if (text[j] === ";") return j + 1;
				return i;
			}
			continue;
		}
		if (c === ";" && depth === 0) { return i + 1; }
		i++;
	}
	throw new Error("Unterminated statement from index " + start);
}

let out = src;
// Remove from last anchor to first so earlier indices stay valid.
const cuts = ANCHORS.map((anchor) => {
	const start = out.indexOf(anchor);
	if (start === -1) {
		throw new Error(
			`Anchor not found — code.js drifted: ${JSON.stringify(anchor)}. Re-derive ANCHORS in build-autodocs-runtime.mjs.`,
		);
	}
	const end = endOfStatement(out, start);
	return { anchor, start, end };
}).sort((a, b) => b.start - a.start);

for (const { start, end } of cuts) {
	out = out.slice(0, start) + "/* [autodocs-runtime] init statement removed */" + out.slice(end);
}

// Sanity: none of the bridge-breaking calls survive at top level
for (const bad of ["figma.showUI(", "figma.ui.onmessage ="]) {
	if (out.includes(bad)) {
		throw new Error(`Strip incomplete — '${bad}' still present in output.`);
	}
}

const header = `// GENERATED by scripts/build-autodocs-runtime.mjs — DO NOT EDIT.
// Source: src/vendor/obra-autodocs/code.js (Obra Autodocs plugin).
// Three top-level UI-init statements stripped; hoisted declarations retained.
// The MCP tool appends a font-load + generate()/remove entry at call time.
`;
const body = header + out;
writeFileSync(OUT, body, "utf8");

// Validate syntax by checking the .js (it is a valid standalone script).
try {
	execSync(`node --check "${OUT}"`, { stdio: "pipe" });
} catch (e) {
	throw new Error("Output failed node --check: " + (e.stderr?.toString() || e.message));
}

// Emit a TS module that EMBEDS the body as a JSON-escaped string. Only dist/
// ships in the published package, so src/vendor assets must be compiled in.
// JSON.stringify is used (not a template literal) so the plugin's own backticks
// and ${...} sequences are safely escaped.
const TS_OUT = join(VENDOR, "autodocs-body.generated.ts");
const ts = `// GENERATED by scripts/build-autodocs-runtime.mjs — DO NOT EDIT.
// Embeds the stripped Obra Autodocs plugin body (vendored code.js) as a string
// so it compiles into dist/ and ships with the package.
/* eslint-disable */
export const AUTODOCS_BODY: string = ${JSON.stringify(body)};
`;
writeFileSync(TS_OUT, ts, "utf8");

const removed = cuts.length;
console.log(`autodocs-body.js + autodocs-body.generated.ts written (${out.split("\n").length} lines, ${removed} init statements removed, syntax OK).`);
